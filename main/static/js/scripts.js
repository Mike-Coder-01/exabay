function parseMoney(value) {
  return Number.parseFloat(String(value || "0").replace(/,/g, "")) || 0;
}

function formatMoney(value) {
  return value.toLocaleString("en-US", {
    minimumFractionDigits: 2,
    maximumFractionDigits: 2
  });
}

function getCsrfToken() {
  return document.querySelector("[name=csrfmiddlewaretoken]")?.value || "";
}

async function syncCart(item) {
  const action = item.dataset.pendingAction;
  const url = item.dataset.pendingUrl;

  if (!url) return;

  const quantityInput = item.querySelector("[data-cart-quantity]");

  const payload = {
    action,
    quantity: Number.parseInt(quantityInput?.value, 10) || 1,
  };

  try {
    const response = await fetch(url, {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
        "X-CSRFToken": getCsrfToken(),
      },
      body: JSON.stringify(payload),
    });

    if (!response.ok) {
      throw new Error("Cart update failed");
    }

    return await response.json();

  } catch (error) {
    console.error(error);
  }
}

function updateCartTotals() {
  if (!cartPage) return;

  let itemCount = 0;
  let cartTotal = 0;

  cartPage.querySelectorAll("[data-cart-item]").forEach((item) => {
    const price = parseMoney(item.dataset.price);

    const quantityInput = item.querySelector("[data-cart-quantity]");

    const quantity = Math.max(
      1,
      Number.parseInt(quantityInput.value, 10) || 1
    );

    const subtotal = price * quantity;

    item.dataset.quantity = String(quantity);

    quantityInput.value = String(quantity);

    item.querySelector("[data-cart-subtotal]").textContent =
      formatMoney(subtotal);

    itemCount += quantity;
    cartTotal += subtotal;
  });

  cartPage
    .querySelectorAll("[data-cart-total], [data-cart-grand-total]")
    .forEach((element) => {
      element.textContent = formatMoney(cartTotal);
    });

  const countElement = cartPage.querySelector("[data-cart-count]");

  if (countElement) {
    countElement.textContent =
      `${itemCount} item${itemCount === 1 ? "" : "s"}`;
  }
}

function queueCartAjax(item, action) {
  const url =
    action === "remove"
      ? item.dataset.removeUrl
      : item.dataset.updateUrl;

  item.dataset.pendingAction = action;
  item.dataset.pendingUrl = url || "";
}

function initializeCartPage() {
  if (!cartPage) return;

  cartPage.addEventListener("click", async (event) => {
    const item = event.target.closest("[data-cart-item]");

    if (!item) return;

    const input = item.querySelector("[data-cart-quantity]");

    const max =
      Number.parseInt(input.getAttribute("max"), 10) || Infinity;

    const currentValue =
      Number.parseInt(input.value, 10) || 1;

    // Increase quantity
    if (event.target.closest("[data-cart-increase]")) {

      input.value = String(
        Math.min(max, currentValue + 1)
      );

      queueCartAjax(item, "update");

      await syncCart(item);

      updateCartTotals();
    }

    // Decrease quantity
    if (event.target.closest("[data-cart-decrease]")) {

      input.value = String(
        Math.max(1, currentValue - 1)
      );

      queueCartAjax(item, "update");

      await syncCart(item);

      updateCartTotals();
    }

    // Remove item
    if (event.target.closest("[data-cart-remove]")) {

      queueCartAjax(item, "remove");

      await syncCart(item);

      item.classList.add("is-removing");

      window.setTimeout(() => {
        item.remove();
        updateCartTotals();
      }, 160);
    }
  });

  cartPage.addEventListener("change", async (event) => {

    if (!event.target.matches("[data-cart-quantity]")) return;

    const input = event.target;

    const item = input.closest("[data-cart-item]");

    const max =
      Number.parseInt(input.getAttribute("max"), 10) || Infinity;

    const value =
      Number.parseInt(input.value, 10) || 1;

    input.value = String(
      Math.min(max, Math.max(1, value))
    );

    queueCartAjax(item, "update");

    await syncCart(item);

    updateCartTotals();
  });

  updateCartTotals();
}

function initializeAuthRoleForms() {
  document.querySelectorAll("[data-auth-role-form]").forEach((form) => {
    const roleInputs = form.querySelectorAll('input[name="role"]');
    const sellerFields = form.querySelector("[data-seller-fields]");

    function syncRoleState() {
      const selectedRole = form.querySelector('input[name="role"]:checked')?.value || "buyer";

      roleInputs.forEach((input) => {
        const card = input.closest(".auth-role__card");
        if (card) {
          card.classList.toggle("is-selected", input.checked);
        }
      });

      if (sellerFields) {
        sellerFields.classList.toggle("is-hidden", selectedRole !== "seller");
      }
    }

    roleInputs.forEach((input) => {
      input.addEventListener("change", syncRoleState);
    });

    syncRoleState();
  });
}


function initializeFooterNewsletter() {
  document.querySelectorAll("[data-footer-newsletter]").forEach((form) => {
    const input = form.querySelector('input[type="email"]');
    const status = form.querySelector("[data-footer-newsletter-status]");
    const button = form.querySelector('button[type="submit"]');
    const spinner = button.querySelector(".spinner");
    const buttonText = button.querySelector(".btn-text");

    form.addEventListener("submit", async (event) => {
      event.preventDefault();

      if (!input.checkValidity()) {
        input.reportValidity();
        return;
      }

      const formData = new FormData(form);

      const csrfToken = document.cookie
        .split("; ")
        .find(row => row.startsWith("csrftoken="))
        ?.split("=")[1];

      // show spinner (NO CSS needed)
      button.disabled = true;
      spinner.style.display = "inline-flex";
      buttonText.textContent = "Subscribing...";
      status.textContent = "";

      try {
        const response = await fetch("/users/subscribe/", {
          method: "POST",
          body: formData,
          headers: {
            "X-CSRFToken": csrfToken,
          },
        });

        const data = await response.json();

        if (data.error) {
          status.textContent = data.error;
        } else if (data.message) {
          status.textContent = data.message;
          form.reset();
        }

      } catch (error) {
        status.textContent = "Sorry, we could not subscribe you right now.";
      } finally {
        button.disabled = false;
        spinner.style.display = "none";
        buttonText.textContent = "Subscribe";
      }
    });
  });
}

